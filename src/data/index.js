// ─── CAR GENERATOR ────────────────────────────────────────────────────────────
function generateCars() {
  let id = 1;
  return BRANDS.flatMap((brand) => {
    const models  = MODELS[brand.id] || [];
    const [minP, maxP] = PRICES[brand.id];
    return models.map((model, i) => ({
      id:        id++,
      brand:     brand.id,
      brandName: brand.name,
      model,
      year:      2023 + (i % 2),
      color:     CAR_COLORS[i % CAR_COLORS.length],
      price:     Math.round((minP + (maxP - minP) * (i / models.length)) / 1000) * 1000,
      engine:    ["1.5T","2.0T","3.0T","V6","V8","Electric","Hybrid"][i % 7],
      mileage:   Math.floor(Math.random() * 25000),
      seats:     [4, 5, 7][i % 3],
      fuel:      ["Petrol","Diesel","Electric","Hybrid"][i % 4],
      power:     [150,200,250,300,400,500,600][i % 7],
      rating:    +(3.5 + Math.random() * 1.5).toFixed(1),
      available: i % 5 !== 0,
      branch:    BRANCHES[i % BRANCHES.length].id,
      isNew:     i % 4 === 0,
    }));
  });
}

export const ALL_CARS = generateCars();
